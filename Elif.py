numero = int(input("ingresa un numero que deseas convertir: "))
if numero == 0 :
    print("numero es cero")
elif numero == 1:
    print("numero es uno")
elif numero == 2:
    print("numero es dos")
elif numero == 3:
    print("numero es tres")
elif numero == 4:
    print("numero es cuatro")
elif numero == 5:
    print("numero es cinco")
else:
    print(" la aplicacion solo funciona hasta el numero cinco")
